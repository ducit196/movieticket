    /*Hàm thêm 1 tài khoản vào trong cơ sở dữ liệu*/
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        /*Các hàm dùng để bắt lỗi nhập sai của người dùng*/
        if (!BatNgoaiLe.loiString(jtfUserName.getText())) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            jtfUserName.requestFocus();
            return;
        }
        if (!BatNgoaiLe.loiString(jpwPass.getText())) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            jpwPass.requestFocus();
            return;
        }
        if (!BatNgoaiLe.loiString(jtfHoTen.getText())) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            jtfHoTen.requestFocus();
            return;
        }
        if (!BatNgoaiLe.loiString(jtfDiaChi.getText())) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            jtfDiaChi.requestFocus();
            return;
        }
        if (!BatNgoaiLe.loiString(jtfSoDT.getText())) {
            JOptionPane.showMessageDialog(null, "Chưa nhập dữ liệu");
            jtfSoDT.requestFocus();
            return;
        }
        
        /*Hỏi người dùng có đồn ý thêm tài khoản vào csdl không*/
        int luaChon = JOptionPane.showConfirmDialog(this, "Thông tin tai khoan\n"
                + "UserName:" + jtfUserName.getText() + "\n"
                + "PassWord:" + jpwPass.getText() + "\n"
                + "Họ tên: " + jtfHoTen.getText() + "\n"
                + "Địa chỉ:" + jtfDiaChi.getText() + "\n"
                + "Số điện thoại: " + jtfSoDT.getText() + "\n"
                + "Chức vụ: " + jcbRole.getSelectedItem().toString());
        if (luaChon == JOptionPane.YES_NO_OPTION) { //Đồng ý ==> thêm vào csdl
            TaiKhoan taiKhoan = new TaiKhoan(0, jtfUserName.getText(), jpwPass.getText(), jtfHoTen.getText(),
                    jtfDiaChi.getText(), jtfSoDT.getText(), jcbRole.getSelectedItem().toString());
            boolean check = taiKhoanDAO.themTaiKhoan(taiKhoan);
            if (check == true) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "UserName đã có người sử dụng!");
                jtfUserName.requestFocus();
                return;
            }
        }
        v = taiKhoanDAO.danhSachTaiKhoan();
        setTBUser();
    }//GEN-LAST:event_jButton1ActionPerformed

    /*Hàm bắt sự kiện click vào bảng dữ liệu*/
    private void jtbUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbUserMouseClicked
        
        /*Khi click vào 1 dòng của bảng thì dữ liệu của bảng đó sẽ đc show lên form*/
        int hang = jtbUser.getSelectedRow();
        if (hang != -1) {
            jtfUserName.setText(jtbUser.getValueAt(hang, 1).toString());
            jpwPass.setText(jtbUser.getValueAt(hang, 2).toString());
            jtfHoTen.setText(jtbUser.getValueAt(hang, 3).toString());
            jtfDiaChi.setText(jtbUser.getValueAt(hang, 4).toString());
            jtfSoDT.setText(jtbUser.getValueAt(hang, 5).toString());
            jcbRole.setSelectedItem(jtbUser.getValueAt(hang, 6));
            idChon = Integer.parseInt(jtbUser.getValueAt(hang, 0).toString());
        }
    }//GEN-LAST:event_jtbUserMouseClicked

