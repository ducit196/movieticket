            TaiKhoan taiKhoan = new TaiKhoan(0, jtfUserName.getText(), jpwPass.getText(), jtfHoTen.getText(),
                    jtfDiaChi.getText(), jtfSoDT.getText(), jcbRole.getSelectedItem().toString());
